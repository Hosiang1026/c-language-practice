#include <stdio.h>

/**
 * 综合练习-5-2
 * 输出: 15 28
 * 
 * ASCII码
 * 
 * '0'为48 'A'为65 'a'为97
 */
void main()
{
    char a[5][10] = 
    {
        "12345", 
        "asde45", 
        "wer45t", 
        "zxc12", 
        "89uy78"
    };
    char *p;
    int i, c = 0, k = 0;
    for (i = 0; i < 5; i++)
    {   //i=0; p=&a[0][0]
        p = &a[i][0];
        while (*p != '\0')
        {   
            if (*p >= '0' && *p <= '9')
                k++;
            c++;
            p++;

            //i = 0; k = 5; c = 5; 
            //*p='1'; '1' >= '0' && '1' <= '9' true k=0+1=1 c=0+1=1 p=1+1=2
            //*p='2'; '2' >= '0' && '2' <= '9' true k=1+1=2 c=1+1=2 p=2+1=3
            //*p='3'; '3' >= '0' && '3' <= '9' true k=2+1=3 c=2+1=3 p=3+1=4
            //*p='4'; '4' >= '0' && '4' <= '9' true k=3+1=4 c=3+1=4 p=4+1=5
            //*p='5'; '5' >= '0' && '5' <= '9' true k=4+1=5 c=4+1=5 p=5+1=6

            //i = 1; k = 2; c = 6; k=5+2=7 c=5+6=11 
            //i = 2; k = 2; c = 6; k=7+2=9 c=11+6=17 
            //i = 3; k = 2; c = 5; k=9+2=11 c=17+5=22 
            //i = 4; k = 4; c = 6; k=11+4=15 c=22+6=28 
 
        } 
    }

    //输出: 15 28
    printf("%d %d\n", k, c);
}
