#include <stdio.h>

/**
 * 综合练习-4-2
 * 输出: 36 26 32
 */
void main()
{
    int a[8] = {12, 15, 20, 8, 5, 9, 16, 10};
    int i, s[3] = {0};
    for (i = 0; i < 8; i++)
        // i=0; s[0]=s[0]+a[0]=12
        // i=1; s[0]=s[0]+a[1]=27
        // i=2; s[2]=s[2]+a[2]=20
        // i=3; s[2]=s[2]+a[3]=28

        // i=4; s[2]=s[2]+a[4]=33
        // i=5; s[0]=s[0]+a[5]=36
        // i=6; s[1]=s[1]+a[6]=16
        // i=7; s[1]=s[1]+a[7]=26
        s[a[i] % 3] += a[i];
    // 输出: 36 26 32
    printf("%d %d %d\n", s[0], s[1], s[2]);
}
