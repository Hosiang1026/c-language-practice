#include <stdio.h>

/**
 * 综合练习-4-3
 * 输出: 16 22 20
 */
void main()
{   
    int a[3][3] = 
    {
        {3, 8, 6}, 
        {5, 9, 2}, 
        {8, 5, 12}
   };
    int i, j, b[3] = {0};
    for (i = 0; i < 3; i++)
        for (j = 0; j < 3; j++)
            b[j] += a[i][j];
        //i=0; j=0; b[0]=b[0]+a[0][0]=0+3=3
        //i=0; j=1; b[1]=b[1]+a[0][1]=0+8=8
        //i=0; j=2; b[2]=b[2]+a[0][2]=0+6=6

        //i=1; j=0; b[0]=b[0]+a[1][0]=3+5=8
        //i=1; j=1; b[1]=b[1]+a[1][1]=8+9=17
        //i=1; j=2; b[2]=b[2]+a[1][2]=6+2=8

        //i=2; j=0; b[0]=b[0]+a[2][0]=8+8=16
        //i=2; j=1; b[1]=b[1]+a[2][1]=17+5=22
        //i=2; j=2; b[2]=b[2]+a[2][2]=8+12=20

    for (j = 0; j < 3; j++)
        printf("%d ", b[j]);
        //j=0; b[0]=16; 输出: 16
        //j=1; b[1]=22; 输出: 22
        //j=2; b[2]=20; 输出: 20
}
